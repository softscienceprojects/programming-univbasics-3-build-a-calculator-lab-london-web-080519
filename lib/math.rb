def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  subs = num1 - num2
  subs
end

def division(num1, num2)
  divs = num1 / num2
  divs
end

def multiplication(num1, num2)
  mults = num1 * num2
  mults
end

def modulo(num1, num2)
  mods = num1 % num2
  mods
end

def square_root(num)
  sqs = num.sqrt
  sqs
end
